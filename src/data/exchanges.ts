export function getExchange(slug: string): Product | undefined {
  return exchanges.find((e) => e.slug === slug);
}

export function getExchangesByBestFor(filter: string[]): Product[] {
  return exchanges.filter((e) =>
    filter.some(
      (f) =>
        e.bestFor.toLowerCase().includes(f.toLowerCase()) ||
        e.description.toLowerCase().includes(f.toLowerCase())
    )
  );
}

export function getTopExchanges(count: number = 5): Product[] {
  return [...exchanges].sort((a, b) => b.rating - a.rating).slice(0, count);
}

export function getExchangeComparison(slugs: string[]): ComparisonItem[] {
  return exchanges
    .filter((e) => slugs.includes(e.slug))
    .map((e) => ({
      name: e.name,
      slug: e.slug,
      features: {
        "Trading Fees": e.fees || "N/A",
        "Best For": e.bestFor,
        "Rating": e.rating.toFixed(1) + "/5.0",
      },
      rating: e.rating,
      affiliateUrl: e.affiliateUrl,
    }));
}

