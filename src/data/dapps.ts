export function getDAppsByLevel(level: DAppLevel): DApp[] {
  const slugs = Object.entries(DAPP_LEVEL_MAP)
    .filter(([, l]) => l === level)
    .map(([slug]) => slug);
  return DAPPS.filter((d) => slugs.includes(d.slug));
}

export function getHomepageDApps(level: DAppLevel): DApp[] {
  const slugs = HOMEPAGE_DAPPS[level];
  return slugs
    .map((slug) => DAPPS.find((d) => d.slug === slug))
    .filter(Boolean) as DApp[];
}
