export function getDAppsByEcosystem(key: EcosystemKey): DApp[] {
  if (key === 'all') return DAPPS;
  const meta = ECOSYSTEMS_META[key];
  if (!meta.chains.length) return DAPPS; // 'all' fallback
  // bitcoin: match by tags/ecosystem field
  if (key === 'bitcoin') {
    return DAPPS.filter((d) =>
      d.ecosystem === 'bitcoin' ||
      d.tags.some((t) => t.toLowerCase().includes('bitcoin') || t.toLowerCase().includes('btc'))
    );
  }
  // multichain: match dApps that span many chains OR have ecosystem='multichain'
  if (key === 'multichain') {
    return DAPPS.filter((d) =>
      d.ecosystem === 'multichain' ||
      d.chains.includes('multi') ||
      d.chains.length >= 5
    );
  }
  return DAPPS.filter((d) =>
    d.ecosystem === key ||
    d.chains.some((c) => meta.chains.includes(c))
  );
}

