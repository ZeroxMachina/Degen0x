// Get today's 10 questions based on date hash
export function getDailyQuestions(dateStr?: string): TriviaQuestion[] {
  const today = dateStr || new Date().toISOString().split("T")[0]; // YYYY-MM-DD
  const seed = today.split("").reduce((acc, c) => acc + c.charCodeAt(0), 0);

  // Shuffle deterministically based on seed
  const shuffled = [...TRIVIA_QUESTIONS];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = (seed * (i + 1) * 2654435761) % (i + 1);
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }

  // Return exactly 10: 3 easy, 4 medium, 3 hard
  const easy = shuffled.filter((q) => q.difficulty === "easy").slice(0, 3);
  const medium = shuffled.filter((q) => q.difficulty === "medium").slice(0, 4);
  const hard = shuffled.filter((q) => q.difficulty === "hard").slice(0, 3);

  // Interleave: easy, medium, easy, medium, hard, medium, easy, hard, medium, hard
  return [easy[0], medium[0], easy[1], medium[1], hard[0], medium[2], easy[2], hard[1], medium[3], hard[2]].filter(Boolean);
}

export function getScoreGrade(score: number, maxScore: number): { grade: string; title: string; emoji: string; color: string } {
  const pct = (score / maxScore) * 100;
  if (pct >= 90) return { grade: "S", title: "Crypto Genius", emoji: "🧠", color: "#f7931a" };
  if (pct >= 75) return { grade: "A", title: "DeFi Degen", emoji: "🦍", color: "#3fb950" };
  if (pct >= 60) return { grade: "B", title: "Seasoned Trader", emoji: "📈", color: "#58a6ff" };
  if (pct >= 40) return { grade: "C", title: "Diamond Hands", emoji: "💎", color: "#bc8cff" };
  return { grade: "D", title: "Paper Hands", emoji: "📄", color: "#8b949e" };
}
